import random


def jogar():

    mensagem_abertura()

    palavra_secreta = definir_palavra_secreta()

    letras_acertadas = ["_" for letra in palavra_secreta]

    enforcou = False
    acertou = False

    erros = 0
    print(letras_acertadas)

    while not enforcou and not acertou:

        chute = definir_chute()
        if chute in palavra_secreta:
            marcar_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas

        print(letras_acertadas)
        print("ainda fatam {} letras!".format(letras_acertadas.count("_")))
    if acertou:
        mensagem_vencedor()
    else:
        mensagem_perdedor(palavra_secreta)


def mensagem_abertura():
    print("Ola sou eu de novo, clarissa")
    print("To começando a deixar a idéia de ser uma I.A para la")
    print("Mas enfim, estamos aqui para jogar forca nao é mesmo?")


def definir_palavra_secreta():
    arquivo = open("palavras.txt", "r")
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()

    return palavra_secreta


def definir_chute():
    chute = input("Qual a letra? ")
    chute = chute.strip().upper()
    return chute


def marcar_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if chute == letra:
            letras_acertadas[index] = letra
        index += 1


